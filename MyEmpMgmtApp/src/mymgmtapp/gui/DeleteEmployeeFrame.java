    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String empNo=txtEmpNo.getText().trim();
                  int answer=JOptionPane.showConfirmDialog(null, "are u sure", "deleting?", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                  if(answer==JOptionPane.YES_OPTION){
        if(empNo.isEmpty()==true)
        {
            JOptionPane.showMessageDialog(null, "Enter emp no ", "worning",JOptionPane.WARNING_MESSAGE);
            
        }
        try
        {
            
            
            
            if(EmpDao.deleteEmp(Integer.parseInt(empNo))==true)
                JOptionPane.showMessageDialog(null, "deleted succcessfully ", "complete",JOptionPane.INFORMATION_MESSAGE);
            else
                JOptionPane.showMessageDialog(null, "id not found ", "worning",JOptionPane.WARNING_MESSAGE);
                
                
            
            
            
            
            
        }
        catch(SQLException ex)
        {
            ex.printStackTrace();
              JOptionPane.showMessageDialog(null, "sql exception ", "worning",JOptionPane.WARNING_MESSAGE);
            
        }
        catch(Exception ex)
        {
            ex.printStackTrace();
              JOptionPane.showMessageDialog(null, "exception ", "worning",JOptionPane.WARNING_MESSAGE);
            
        }
                  }
    }//GEN-LAST:event_jButton1ActionPerformed

