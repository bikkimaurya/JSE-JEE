    private void btnShowAllEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowAllEmpActionPerformed
        // TODO add your handling code here:
        try
        {   
            
       ArrayList <EmpPojo> empList=EmpDao.getAllEmp();
       
       
          
              txtAllEmp.setText("  epm id     |        imp name        |    empsal\n-----------------------------------------------------\n");
              
        for(EmpPojo e:empList)
        {
           int eno=e.getEmpno();
           String ename= e.getEname();
           double sal=e.getEsal();
           String record= eno+"    "+ename+"     "+sal+"\n";
           txtAllEmp.append(record);
     
            
        }
        
        }
        catch(IllegalArgumentException ex)
        {
            JOptionPane.showMessageDialog(null,"Mismatch");
        }
        catch(SQLException ex)
        {
          JOptionPane.showMessageDialog(null,"sql exception");
        }
        catch(Exception ex)
        {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null,"ERROR in displaying the data");
            
        }
        
        
        
        
        
    }//GEN-LAST:event_btnShowAllEmpActionPerformed

