    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
       String pass=txtPwd.getText().trim();
       String pass2 = txtPwd1.getText().trim();
       if(id.isEmpty()==true || pass.isEmpty()==true || pass2.isEmpty()==true)
                {
                    JOptionPane.showMessageDialog(null,"Uesrname or password can not be left blank", "erroe!",JOptionPane.ERROR_MESSAGE);
                    return;
                }
        if(pass.equals(pass2)!=true)
        { 
            JOptionPane.showMessageDialog(null,"input password mismathc", "input formate",JOptionPane.ERROR_MESSAGE);
        return;
        }
        if(pass2.length()<=4)
        {
            JOptionPane.showMessageDialog(null,"password size should be greater than 4", "input formate",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            
            if(UserDAO.updateUser(id, pass)==true)
               JOptionPane.showMessageDialog(null,id+"  Updated", "input formate",JOptionPane.INFORMATION_MESSAGE);
            else
                 JOptionPane.showMessageDialog(null,id+" couldn't update", "input formate",JOptionPane.INFORMATION_MESSAGE);
            
        } 
        catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null,"couldent udated", "sql exception",JOptionPane.INFORMATION_MESSAGE);
        return;
        }
        ChangePasswordFrame changePasswordFrame= new         ChangePasswordFrame();
         changePasswordFrame.setVisible(true);
         this.dispose();

       
    }//GEN-LAST:event_btnUpdateActionPerformed

