private void lookStudent()
{
    
    jcUserId.removeAllItems();
    jcUserId.addItem("......");
    
    
    
     try {
            ArrayList<String> al= PerformanceDAO.getAllStudentId();
            if(al.isEmpty()==true)
            {
                JOptionPane.showMessageDialog(null,"Sorry no student have given test", "NO records!", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            
              
            for(String name: al)
                jcUserId.addItem(name);
            
            
        } catch (SQLException ex) {
            ex.printStackTrace();
            Logger.getLogger(ViewSingleStudentScoreFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
}
















