    private void jcUserIdItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcUserIdItemStateChanged
     if( evt.getStateChange()==ItemEvent.DESELECTED)
         return;
        try {
            ArrayList<String> al = PerformanceDAO.getAllStudentExamId((String)jcUserId.getSelectedItem());
            jcExamId.removeAllItems();
            jcExamId.addItem("......");
          
            for(String id: al)
                jcExamId.addItem(id);
       
            
        } 
        catch (SQLException ex) 
        {
            
            
            ex.printStackTrace();
            Logger.getLogger(ViewSingleStudentScoreFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_jcUserIdItemStateChanged

    private void jcExamIdItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcExamIdItemStateChanged
     
        try {
            if( evt.getStateChange()==ItemEvent.DESELECTED)
                return;
         StudentScorePojo sc=   PerformanceDAO.getScore(jcUserId.getSelectedItem().toString(),jcExamId.getSelectedItem().toString());
        txtLanguage.setText(sc.getLanguage());
        txtPercentage.setText(sc.getPercentage()+"");
        txtLanguage.setEditable(false);
        txtPercentage.setEditable(false);
        
        } 
        catch (SQLException ex) 
        {
            ex.printStackTrace();
            Logger.getLogger(ViewSingleStudentScoreFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
        
    }//GEN-LAST:event_jcExamIdItemStateChanged

