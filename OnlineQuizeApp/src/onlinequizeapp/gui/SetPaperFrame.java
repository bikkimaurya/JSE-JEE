    private void btnSetQuestionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetQuestionActionPerformed
        // TODO add your handling code here:
        String sub=(String)comboSubject.getSelectedItem();
        String tq=(String)comboQuestion.getSelectedItem();
        if(sub.equalsIgnoreCase(".........")||tq.equalsIgnoreCase("........."))
        {
            JOptionPane.showMessageDialog(null,"choose all option","error", JOptionPane.ERROR_MESSAGE);
            return;
        }
       int questions=Integer.parseInt(tq);
       
 int ans=   JOptionPane.showConfirmDialog(null,"are u sure ","conformation",JOptionPane.INFORMATION_MESSAGE,JOptionPane.YES_NO_OPTION);
        if(ans!=JOptionPane.YES_OPTION)
            return;
       ExamPojo obj=new  ExamPojo();
       try{
          String id= ExamDAO.getExamId();
           System.out.println(id);
          obj.setExamId(id);
          obj.setLanguage(sub);
          obj.setTotalQuestions(questions);
          SetQuestionsFrame setQuestionsFrame=new SetQuestionsFrame(obj);
          setQuestionsFrame.setVisible(true);
          dispose();
        
          
         } 
       catch (SQLException ex) {
           ex.printStackTrace();
            JOptionPane.showMessageDialog(null,"error while connectiing to the database", "error", JOptionPane.ERROR);
        }
       
       
       
       
       
       
       
    }//GEN-LAST:event_btnSetQuestionActionPerformed

