    private void btnDoTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoTaskActionPerformed
       
       if (jbrTakeTest.isSelected()==false && jbrViewScore.isSelected()==false && jbrChangePassword.isSelected()==false)
       {
       JOptionPane.showMessageDialog(null ," please select an option");
       return;
       }
       else if(jbrTakeTest.isSelected()==true)
       {
           ChoosePaperFrame takeTestFrame = new ChoosePaperFrame();
           takeTestFrame.setVisible(true);
           this.dispose();
           
       }
       else if (jbrViewScore.isSelected()==true)
               {
           try 
           {
               
                  ArrayList<String> al = PerformanceDAO.getAllStudentExamId(UserProfile.getUserName());
                  if(al.size()==0)
                  {
                      JOptionPane.showMessageDialog(null,"you havent attempted any exam yet!");
                      return;
                  }
                  ViewScoreFrame viewScoreFrame = new ViewScoreFrame();
                  viewScoreFrame.setVisible(true);
                  this.dispose();       
                  
                  
           } 
           
           catch (SQLException ex) {
               ex.printStackTrace();
               
               Logger.getLogger(StudentOptionsFrame.class.getName()).log(Level.SEVERE, null, ex);
           }      
               
               
               }
       else if(jbrChangePassword.isSelected()==true)
       {
           ChangePasswordFrame changePasswordFrame = new ChangePasswordFrame();
           changePasswordFrame.setVisible(true);
           this.dispose();
           
           
       }        
        
    }//GEN-LAST:event_btnDoTaskActionPerformed

