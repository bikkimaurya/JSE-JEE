    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
 
        
        String sub=(String)jcSubject.getSelectedItem();
        String id=(String)jcExamId.getSelectedItem();
        if(sub.equalsIgnoreCase("......")==true||id.equalsIgnoreCase("......"))
        {
            JOptionPane.showMessageDialog(null, "select all values");
            return;
        }
        
        ExamPojo examPojo= new ExamPojo();
        
        examPojo.setExamId(id);
        examPojo.setLanguage(sub);
        try {
            examPojo.setTotalQuestions(ExamDAO.getQ(examPojo) );
        } catch (SQLException ex) {
            ex.printStackTrace();
            System.out.println("in edit paper");
            Logger.getLogger(EditPaperFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        EditQuestionsFrame editQuestionsFrame= new EditQuestionsFrame( examPojo);
        editQuestionsFrame.setVisible(true);
        this.dispose();
        
        
        
    }//GEN-LAST:event_btnEditActionPerformed

