private void add()
{
    
    
        try {
      ArrayList<ExamPojo> al=      ExamDAO.get();
      HashSet<String> hs= new HashSet<>();
       for(ExamPojo e: al)
       {
          hs.add(e.getLanguage());
          
           
       }
       for(String s: hs)
       {
          jcSubject.addItem(s);
       }          
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null,"exptn");
        }
}
